function soma(number1, number2){
    return number1 + number2
}
let resultadoSoma = soma(10,20)
console.log('O resultado de 10 + 20 é igual a ' + resultadoSoma)

function divisao(number1, number2){
    return number1 / number2
}
let resultadoDivisao = divisao(50,10)
console.log('O resultado de 50 : 10 é igual a ' + resultadoDivisao)

function multiplicacao(number1, number2, number3){
    return number1 * number2 * number3
}
let resultadoMultiplicacao = multiplicacao(10, 2, 3)
console.log('O resultado de 10 x 2 x 3 é igual a ' + resultadoMultiplicacao)

function subtracao(number1, number2){
    return number1 - number2
}
let resultadoSubtracao = subtracao(-5, 6)
console.log('O resultado de -5 -6 é igual a ' + resultadoSubtracao)